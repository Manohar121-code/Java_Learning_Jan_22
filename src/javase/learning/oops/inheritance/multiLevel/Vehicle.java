package javase.learning.oops.inheritance.multiLevel;

public class Vehicle {

	private int numOfTires;
	private int numOfGears;

	public int getNumOfTires() {
		return numOfTires;
	}

	public void setNumOfTires(int numOfTires) {
		this.numOfTires = numOfTires;
	}

	public int getNumOfGears() {
		return numOfGears;
	}

	public void setNumOfGears(int numOfGears) {
		this.numOfGears = numOfGears;
	}

}
