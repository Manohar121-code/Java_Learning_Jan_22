package javase.learning.basics.operators;

public class RelationalOperatorsEx {
	public static void main(String[] args) {
		int i = 20;
		
		if(i == 20) {
			System.out.println("if block executed - i == 20");
		}
		
		if (i != 15) {
			System.out.println("if block executed - i != 15");
		}
		
		if (i < 15) {
			System.out.println("if block executed - i < 15");
		}
		
		if (i <= 20) {
			System.out.println("if block executed - i <= 20");
		}
		
		if (i > 15) {
			System.out.println("if block executed - i > 15");
		}
		
		if (i >= 20) {
			System.out.println("if block executed - i >= 20");
		}
	}
}
